#include "dog.h"
#include <stdlib.h>

/**
 * dog_t - intializes a variable of type dog
 * @d: type dog
 * @name: of the dog
 * @age: its age
 * @owner: its owner
 * Return: no return
 **/

dog_t *new_dog(char *name, float age, char *owner)
{
	if (d != NULL)
	{
		(*d).name = name;
		d->age = age;
		(*d).owner = owner;
	}
}
